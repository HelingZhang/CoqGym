Fixpoint exp (base power : nat) : nat :=
  match power with
    | O => S O
    | S p => mult base (exp base p)
  end.


Fixpoint factorial (n:nat) : nat :=
  match n with
    | 0 => 1
    | S n' => mult (S n') (factorial n')
  end.

